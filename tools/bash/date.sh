#!/bin/bash

# 得到时间戳，从1970年1月1日00:00:00到目前经历的秒数 
date +%s

# 从时间戳得到日期
# 结果是：五  5  9 21:06:41 HKT 2014
date -d @1399640801
# 结果是：2014-05-09 21:06:41 +0800
date -d '1970-01-01 UTC 1399640801 seconds' +"%Y-%m-%d %T %z"

# 结果是：20140509
date +"%Y%m%d"

# 得到上个月的日期，结果是：20140409
date -d last-month +"%Y%m%d"

